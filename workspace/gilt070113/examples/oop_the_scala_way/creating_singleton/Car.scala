class Marker(val color : String) {
  override def toString = s"Marker with $color"
}

val marker1 = new Marker("Purple")
println(marker1)

object MarkerFactory {
  def red = new Marker("red")
  def blue = new Marker("blue")
  def green = new Marker("green")
}

println(MarkerFactory.red)
println(MarkerFactory.blue)